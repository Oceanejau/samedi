#include "minishell.h"

int	ft_is_nb(char c)
{
	if (c < '0' || c > '9')
		return (-1);
	return (1);
}

int	ft_is_alpha(char c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (1);
	return (-1);
}
