#include "minishell.h"

int	str_c(char *str, char c)
{
	int	x;

	x = 0;
	if (c == '\0')
		return (7);
	while (str[x] != '\0')
	{
		if (str[x] == c)
			return (x);
		x++;
	}
	return (10);
}
